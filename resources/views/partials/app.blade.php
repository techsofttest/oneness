  function getDeviceToken() {
    let token = localStorage.getItem('device_fingerprint');
    if (!token) {
      token = crypto.randomUUID();  // modern browsers support this
      localStorage.setItem('device_fingerprint', token);
    }
    return token;
  }

  // On form submit, add device_token to a hidden input
  document.addEventListener('DOMContentLoaded', () => {
    const loginForm = document.querySelector('#loginForm');
    const input = document.createElement('input');
    input.type = 'hidden';
    input.name = 'device_fingerprint';
    input.value = getDeviceToken();
    loginForm.appendChild(input);
  });
